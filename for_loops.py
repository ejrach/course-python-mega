# Dictionaries
pins = {"Mike":1234, "Joe":1111, "Jack":2222}
for item in pins:
    # print("Key: ", pins.keys(), "Value: ", pins.value())
    # prints the key
    print(item)

for item in pins.keys():
    # print("Key: ", pins.keys(), "Value: ", pins.value())
    # prints the key
    print(item)

for item in pins.values():
    # print("Key: ", pins.keys(), "Value: ", pins.value())
    # prints the value
    print(item)



